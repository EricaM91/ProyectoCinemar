package Peliculas;

public class Genero {

	private int Genero;
	private String Nombre;
	
	public Genero(int genero, String nombre) {
		
		Genero = genero;
		Nombre = nombre;
			
		
	}

	public int getGenero() {
		return Genero;
	}

	public void setGenero(int genero) {
		Genero = genero;
	}

	public String getNombre() {
		return Nombre;
	}

	public void setNombre(String nombre) {
		Nombre = nombre;
	}
	
	
	
	
	
	
	
}
